export const Regex = {
	email:
		/^(?=.{1,150}$)(?=[^@]{3,40}@)[A-Za-z](?:[A-Za-z0-9_%+-]|\.(?!\.|@)){3,50}@(?:[A-Za-z0-9](?:[A-Za-z0-9-]{0,50}[A-Za-z0-9])?\.)+[A-Za-z]{2,10}$/,
	password:
		/^(?=.{8,}$)(?=.*\d)(?=.*[@#$%^&<>,.?\\])(?!.*[+\-*/_\s(){}[\]!:;"'|~`=])[A-Za-z\d@#$%^&<>,.?\\]+$/,
	phone: /^(0|\+84)[3|5|7|8|9][0-9]{8}$/,
};
